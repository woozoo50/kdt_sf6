# 연산자(operator)
'''
산술 연산 + , - , * , / , // , % 


'''
n1 = 10
n2 = 4

print(n1, n2)
print(n1 + n2)
print(n1 - n2)
print(n1 * n2)
print(n1 / n2)
print(n1 // n2)
print(n1 % n2)
print(n1 ** n2)

total_bread = 30
number_man = 4

quo = total_bread // number_man
remain = total_bread % number_man
print("빵의 개수 : " ,quo)
print("남은 빵의 개수 : " ,remain)
